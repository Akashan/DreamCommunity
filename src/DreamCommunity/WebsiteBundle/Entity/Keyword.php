<?php

namespace DreamCommunity\WebsiteBundle\Entity;


class Keyword
{

    private $name;


    /**
     * Set urlVideo
     *
     * @param string $name
     * @return Video
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

}